import os
import sys
import argparse
import json
import pandas as pd
import numpy as np
import lightgbm as lgb
from sklearn.metrics import mean_squared_error
from pipeline_utils import CAT_COLS, lightgbm_gpu_params
from validation import get_validation_splits, validate_fold_partition
from rewards import append_reward_event
import features


def _load_eval_frame(data_dir):
    print("Generating features...")
    try:
        features_df = features.create_features(data_dir=os.path.join(data_dir, 'inputs')).to_pandas()
    except Exception as e:
        print(f"Error during feature generation: {e}")
        raise

    train = pd.read_csv(os.path.join(data_dir, 'inputs', 'Train.csv'))
    df = train.merge(features_df, on='UniqueID', how='left')

    for c in CAT_COLS:
        if c in df.columns:
            df[c] = df[c].astype('category')

    drop_cols = ['UniqueID', 'BirthDate', 'next_3m_txn_count']
    feature_cols = [c for c in df.columns if c not in drop_cols]

    X = df[feature_cols]
    y = np.log1p(df['next_3m_txn_count'])
    return df, X, y


def _proxy_params():
    params = {
        'objective': 'regression',
        'metric': 'rmse',
        'learning_rate': 0.05,
        'num_leaves': 31,
        'max_depth': 6,
        'verbose': -1,
        'random_state': 42
    }

    ### AUTOGENERATED HYPERPARAMETERS ###
    params["learning_rate"] = 0.03
    params["max_depth"] = 7
    params["num_leaves"] = 25
    params["min_child_samples"] = 30
    params["colsample_bytree"] = 0.7
    params["reg_alpha"] = 0.1
    params["reg_lambda"] = 0.1
    ### END AUTOGENERATED HYPERPARAMETERS ###

    try:
        params.update(lightgbm_gpu_params())
    except Exception:
        pass
    return params


def _fit_fold(X, y, folds, fold_idx):
    if fold_idx < 0 or fold_idx >= len(folds):
        raise ValueError(f"fold_idx must be between 0 and {len(folds) - 1}; got {fold_idx}.")

    train_idx, val_idx = folds[fold_idx]

    X_train, y_train = X.iloc[train_idx], y.iloc[train_idx]
    X_val, y_val = X.iloc[val_idx], y.iloc[val_idx]

    train_data = lgb.Dataset(X_train, label=y_train, categorical_feature=[c for c in CAT_COLS if c in X.columns])
    val_data = lgb.Dataset(X_val, label=y_val, categorical_feature=[c for c in CAT_COLS if c in X.columns])

    params = _proxy_params()

    print(f"Training Fold {fold_idx} LightGBM proxy...")
    model = lgb.train(
        params,
        train_data,
        num_boost_round=1000,
        valid_sets=[train_data, val_data],
        callbacks=[lgb.early_stopping(stopping_rounds=30, verbose=False)]
    )

    preds = model.predict(X_val)
    fold_rmse = np.sqrt(mean_squared_error(y_val, preds))
    print(f"Fold {fold_idx} Fast Eval RMSLE: {fold_rmse:.5f}")

    importances = model.feature_importance(importance_type='gain')
    feature_names = model.feature_name()
    return float(fold_rmse), pd.Series(importances, index=feature_names, dtype=np.float64)


def _top_features_from_importances(importances):
    if not importances:
        return []
    total = pd.concat(importances, axis=1).fillna(0.0).sum(axis=1)
    return total.sort_values(ascending=False).head(10).index.tolist()


def parse_fold_list(value):
    if value is None or str(value).strip() == "":
        return None
    folds = []
    for part in str(value).split(","):
        part = part.strip()
        if not part:
            continue
        folds.append(int(part))
    if not folds:
        raise ValueError("--folds must include at least one fold index.")
    return folds


def run_fast_eval(data_dir='data', log_reward=False, baseline_score=None, fold_idx=0):
    print(f"Running fast feature evaluation on Fold {fold_idx}...")
    try:
        df, X, y = _load_eval_frame(data_dir)
        folds = get_validation_splits(df, y, n_splits=5, random_state=42)
        validate_fold_partition(folds, len(df))
        score, importances = _fit_fold(X, y, folds, fold_idx)
        top_features = _top_features_from_importances([importances])
    except Exception as e:
        print(f"Fast eval failed: {e}")
        return float('inf'), []

    if log_reward and baseline_score is not None:
        improvement = baseline_score - score
        append_reward_event(
            event_type="agent_feature_gen",
            scenario="fast_eval",
            improvement=improvement,
            old_score=baseline_score,
            new_score=score
        )
    
    return score, top_features


def run_fast_eval_folds(data_dir='data', fold_indices=None, log_reward=False, baseline_score=None):
    fold_indices = fold_indices or [0, 1, 2, 3, 4]
    print(f"Running fast feature evaluation on Folds {fold_indices}...")
    try:
        df, X, y = _load_eval_frame(data_dir)
        folds = get_validation_splits(df, y, n_splits=5, random_state=42)
        validate_fold_partition(folds, len(df))
        scores = {}
        importances = []
        for fold_idx in fold_indices:
            score, fold_importances = _fit_fold(X, y, folds, int(fold_idx))
            scores[int(fold_idx)] = score
            importances.append(fold_importances)
        avg_score = float(np.mean(list(scores.values()))) if scores else float("inf")
        top_features = _top_features_from_importances(importances)
    except Exception as e:
        print(f"Fast eval failed: {e}")
        return {}, float('inf'), []

    if log_reward and baseline_score is not None and np.isfinite(avg_score):
        improvement = baseline_score - avg_score
        append_reward_event(
            event_type="agent_feature_gen",
            scenario="fast_eval_multi_fold",
            improvement=improvement,
            old_score=baseline_score,
            new_score=avg_score
        )

    return scores, avg_score, top_features

if __name__ == "__main__":
    parser = argparse.ArgumentParser()
    parser.add_argument("--baseline", type=float, default=None, help="Baseline RMSLE to compare against")
    parser.add_argument("--log-reward", action="store_true", help="Whether to log the reward")
    parser.add_argument("--fold", type=int, default=0, help="Validation fold index to evaluate (0-4)")
    parser.add_argument("--folds", default=None, help="Comma-separated fold indexes to evaluate, e.g. 0,1,2")
    args = parser.parse_args()

    fold_indices = parse_fold_list(args.folds)
    if fold_indices is None:
        score, top_features = run_fast_eval(
            baseline_score=args.baseline,
            log_reward=args.log_reward,
            fold_idx=args.fold,
        )
        print(f"TOP_FEATURES={','.join(top_features)}")
        print(f"FINAL_SCORE={score}")
    else:
        fold_scores, avg_score, top_features = run_fast_eval_folds(
            baseline_score=args.baseline,
            log_reward=args.log_reward,
            fold_indices=fold_indices,
        )
        print(f"FOLD_SCORES_JSON={json.dumps(fold_scores, sort_keys=True)}")
        print(f"FINAL_AVG={avg_score}")
        print(f"TOP_FEATURES={','.join(top_features)}")
        print(f"FINAL_SCORE={avg_score}")

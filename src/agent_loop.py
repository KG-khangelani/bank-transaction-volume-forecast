FEATURES_FILE = "src/features.py"
FAST_EVAL_FILE = "src/fast_eval.py"

def read_features_file():
    with open(FEATURES_FILE, "r", encoding="utf-8") as f:
        return f.read()

def write_features_file(content):
    with open(FEATURES_FILE, "w", encoding="utf-8") as f:
        f.write(content)

def extract_autogen_block(content):
    pattern = r"(### AUTOGENERATED FEATURES ###\n)(.*?)(\n\s*### END AUTOGENERATED FEATURES ###)"
    match = re.search(pattern, content, flags=re.DOTALL)
    if match:
        return match.group(2)
    return ""

def inject_autogen_block(content, new_block):
    pattern = r"(### AUTOGENERATED FEATURES ###\n)(.*?)(\n\s*### END AUTOGENERATED FEATURES ###)"
    replacement = f"\\1{new_block}\\3"
    return re.sub(pattern, replacement, content, flags=re.DOTALL)

def run_evaluation(baseline_score):
    try:
        cmd = [sys.executable, FAST_EVAL_FILE, "--baseline", str(baseline_score), "--log-reward"]
        result = subprocess.run(cmd, capture_output=True, text=True, check=True)
        # Parse output for FINAL_SCORE
        for line in result.stdout.splitlines():
            if line.startswith("FINAL_SCORE="):
                return float(line.split("=")[1])
        print("Failed to parse FINAL_SCORE from output")
        return float("inf")
    except subprocess.CalledProcessError as e:
        print(f"Evaluation failed: {e.stderr}")
        return float("inf")

